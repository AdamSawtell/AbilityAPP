/**
 * Generates bulk support-worker org positions (32) under site team leaders.
 * Run: node scripts/generate-org-bulk-positions.mjs
 */

import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

const teams = [
  {
    id: "pos-team-northern",
    title: "Team Leader — Northern SIL",
    locationId: "loc-northern-sil",
    site: "Northern SIL",
    sortOrder: 35,
    swStart: 1,
    swEnd: 11,
  },
  {
    id: "pos-team-southern",
    title: "Team Leader — Southern Respite",
    locationId: "loc-southern-respite",
    site: "Southern Respite",
    sortOrder: 36,
    swStart: 12,
    swEnd: 22,
  },
  {
    id: "pos-team-murray",
    title: "Team Leader — Murray Bridge Day",
    locationId: "loc-murray-bridge-day",
    site: "Murray Bridge",
    sortOrder: 37,
    swStart: 23,
    swEnd: 32,
  },
];

function swNum(n) {
  return String(n).padStart(3, "0");
}

const teamPositions = teams.map((t) => ({
  id: t.id,
  title: t.title,
  securityRoleId: "role-team-leader",
  department: "Operations",
  businessArea: "Operations",
  locationId: t.locationId,
  parentPositionId: "pos-gm-ops",
  sortOrder: t.sortOrder,
  chartTier: 6,
  status: "vacant",
  site: t.site,
  costCentre: "CC-OPS",
  primaryEmployeeId: "",
}));

const swPositions = [];
const swAssignments = [];

for (const team of teams) {
  for (let n = team.swStart; n <= team.swEnd; n += 1) {
    const empId = `emp-sw-${swNum(n)}`;
    const posId = `pos-sw-${swNum(n)}`;
    swPositions.push({
      id: posId,
      title: "Support Worker",
      securityRoleId: "role-support-worker",
      department: "Operations",
      businessArea: "Operations",
      locationId: team.locationId,
      parentPositionId: team.id,
      sortOrder: n * 10,
      chartTier: 7,
      status: "filled",
      site: team.site,
      costCentre: "CC-OPS",
      primaryEmployeeId: empId,
    });
    swAssignments.push({
      id: `pa-sw-${swNum(n)}-primary`,
      positionId: posId,
      employeeId: empId,
      assignmentType: "primary",
      effectiveFrom: "2024-01-15",
      effectiveTo: "",
      notes: "Bulk seed primary assignment",
    });
  }
}

const allPositions = [...teamPositions, ...swPositions];
const allAssignments = swAssignments;

function toTsPosition(p) {
  return `  {
    id: "${p.id}",
    title: "${p.title}",
    securityRoleId: "${p.securityRoleId}",
    department: "${p.department}",
    businessArea: "${p.businessArea}",
    locationId: "${p.locationId}",
    parentPositionId: "${p.parentPositionId}",
    sortOrder: ${p.sortOrder},
    chartTier: ${p.chartTier},
    status: "${p.status}",
    site: "${p.site}",
    costCentre: "${p.costCentre}",
    primaryEmployeeId: "${p.primaryEmployeeId}",
  }`;
}

function toTsAssignment(a) {
  return `  {
    id: "${a.id}",
    positionId: "${a.positionId}",
    employeeId: "${a.employeeId}",
    assignmentType: "${a.assignmentType}",
    effectiveFrom: "${a.effectiveFrom}",
    effectiveTo: "${a.effectiveTo}",
    notes: "${a.notes}",
  }`;
}

const tsContent = `/**
 * Bulk support-worker org positions — generated by scripts/generate-org-bulk-positions.mjs
 * Do not edit by hand; re-run the script after changing team/SW mapping.
 */

import type { OrgPositionRecord, PositionAssignmentRecord } from "@/lib/org-structure";

export const bulkOrgPositions: OrgPositionRecord[] = [
${allPositions.map(toTsPosition).join(",\n")},
];

export const bulkPositionAssignments: PositionAssignmentRecord[] = [
${allAssignments.map(toTsAssignment).join(",\n")},
];
`;

function sqlVal(v) {
  if (v == null) return "null";
  return `'${String(v).replace(/'/g, "''")}'`;
}

function sqlText(v) {
  if (v == null) return "''";
  return `'${String(v).replace(/'/g, "''")}'`;
}

const sqlPositionRows = allPositions.map(
  (p) =>
    `  (${sqlVal(p.id)}, ${sqlText(p.title)}, ${sqlVal(p.securityRoleId)}, ${sqlText(p.department)}, ${sqlText(p.businessArea)}, ${sqlVal(p.locationId)}, ${sqlVal(p.parentPositionId)}, ${p.sortOrder}, ${p.chartTier}, ${sqlVal(p.status)}, ${sqlText(p.site)}, ${sqlText(p.costCentre)}, ${p.primaryEmployeeId ? sqlVal(p.primaryEmployeeId) : "null"})`
);

const sqlAssignmentRows = allAssignments.map(
  (a) =>
    `  (${sqlVal(a.id)}, ${sqlVal(a.positionId)}, ${sqlVal(a.employeeId)}, ${sqlVal(a.assignmentType)}, ${sqlVal(a.effectiveFrom)}, null, ${sqlVal(a.notes)})`
);

const sqlFragment = `
-- Bulk support workers under site team leaders (generated)
insert into public.org_position (
  id, title, security_role_id, department, business_area, location_id, parent_position_id, sort_order, chart_tier, status, site, cost_centre, primary_employee_id
) values
${sqlPositionRows.join(",\n")}
on conflict (id) do update set
  title = excluded.title,
  security_role_id = excluded.security_role_id,
  department = excluded.department,
  business_area = excluded.business_area,
  location_id = excluded.location_id,
  parent_position_id = excluded.parent_position_id,
  sort_order = excluded.sort_order,
  chart_tier = excluded.chart_tier,
  status = excluded.status,
  site = excluded.site,
  cost_centre = excluded.cost_centre,
  primary_employee_id = excluded.primary_employee_id,
  updated_at = now();

-- Reparent legacy Oliver support worker under Northern team
update public.org_position
set parent_position_id = 'pos-team-northern', sort_order = 5, updated_at = now()
where id = 'pos-support-worker';

insert into public.position_assignment (
  id, position_id, employee_id, assignment_type, effective_from, effective_to, notes
) values
${sqlAssignmentRows.join(",\n")}
on conflict (id) do update set
  position_id = excluded.position_id,
  employee_id = excluded.employee_id,
  assignment_type = excluded.assignment_type,
  effective_from = excluded.effective_from,
  effective_to = excluded.effective_to,
  notes = excluded.notes,
  updated_at = now();
`;

writeFileSync(join(root, "web/src/lib/org-structure-bulk-positions.ts"), tsContent);
writeFileSync(join(root, "supabase/seed-org-structure-bulk.sql"), sqlFragment.trim() + "\n");

console.log(
  `Wrote ${allPositions.length} positions and ${allAssignments.length} assignments to org-structure-bulk-positions.ts and seed-org-structure-bulk.sql`
);
